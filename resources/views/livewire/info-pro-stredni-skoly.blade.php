            <p>Tak se nám ten nábor na střední školy letos "malinko" komplikuje. Vypadá to,
                že se kvůli COVIDu letos nebudou konat žádné prezenční výstavy středních škol a
                moc nadějně to nevypadá ani s klasickými dny otevřených dveří.</p>
            <p>Naši studenti však opět překvapili a naprogramovali portál pro burzy škol online.</p>
            <p>Využili toho, že v podstatě všechny školy byly na jaře donuceny k zavedení
                nějaké formy dálkové výuky a vyzkoušely si buď Microsoft Teams, Google Meets,
                Zoom nebo něco podobného. No a proč to samé nevyužít pro on-line výstavy?</p>
            <p>Žáci základních škol a jejich rodiče jsou připraveni, že budou v určité termíny
                vybírat, co dále po základce. Střední školy byly připraveny, že budou v ty samé
                termíny informace deváťákům předávat. COVID nám znemožnil pouze komunikaci face
                to face, tak to prostě pojďme obejít pomocí technologií.</p>
            <h2 class="header">Jak to celé funguje?</h2>
            <p>Střední škola se na portále zaregistruje, vyplní o sobě základní informace a
                především obory, které se zde učí.</p>
            <p>Vybere si "výstavy", na kterých by chtěla mluvit s deváťáky a na tyto termíny
                si založí "schůzku" ve svých Teamsech/Meetsech/Zoomech/... podle toho, co používáte.
                (My to děláme v Teamsech tak, že do Účastníků schůzky přidáme nějaký soukromý
                e-mail, který není navázaný na microsoftí účet. Na e-mail pak přijde ODKAZ na schůzku.)</p>
            <p>Objednáte si vybrané výstavy a ke každé z nich vložíte odkaz na schůzku v
                Teamsech/Meetsech/Zoomech/... (Každé výstavě jsme přiřadili dvě časová okna:
                ranní 8:00-12:00 – tam by se měli připojovat deváťáci přímo ze základek a potom
                večerní 18:00-21:00 – pro připojení deváťáků i s rodiči z domova).</p>
            <p>No a to je vše. 🙂 Takže nic složitého. Veškerá komunikace běží přes vaše
                standardní on-line prostředí. To mimo jiné znamená, že všechna data z komunikace
                - např. chat, případný video záznam z celé vaší komunikace, maily účastníků,...
                jsou jenom vaše a portál BurzaŠkol.Online se k nim technicky nedostane. Portál
                pouze "zprostředkuje" deváťákům odkaz na vaši komunikaci.</p>
            <h2 class="header">Co to středním školám a učilištím přinese?</h2>
            <ul class="marked-list">
                <li>Především, letos ta propagace asi moc jinak nepůjde,</li>
                <li><b>Úsporu nákladů</b>
                    <ul>
                        <li>odpadá <b>pronájem výstavní plochy</b>,</li>
                        <li>odpadá <b>tisk letáků/brožurek</b> - budou ke stažení přímo u informací o škole,</li>
                        <li>odpadá <b>cestování po výstavách</b> a náklady s tím spojené,</li>
                    </ul>
                </li>

                <li>Efektivní vynaložení času
                    <ul>
                        <li>odpadá <b>cestování na výstavy</b>,</li>
                        <li>doba strávená na výstavách se zkrátí, <b>všechny výstavy jsou pouze jednodenní</b>,</li>
                        <li>zvládnete i <b>dvě výstavy najednou</b> <i>(v Pardubickém kraji mají například
                                Svitavy a Chrudim stejné datum konání)</i>,
                        </li>
                        <li>sedíte ve své kanceláři, takže pokud právě nemáte on-line návštěvníka,
                            <b>můžete se věnovat jiné práci</b>,
                        </li>
                    </ul>
                </li>
                <li>Odpadá stavění a bourání "výstavních stánků",</li>
                <li>Návštěvníci, se kterými mluvíte, už mají o vaši školu <b>skutečný zájem</b>. Odpadá
                    přemlouvání budoucích kovářů na gymnázia a obráceně.
                </li>
                <li>Pokud je vaše nabídka skutečně jedinečná, můžete se účastnit i výstav, kam
                    normálně nejezdíte. <b>Oslovíte zájemce i ze vzdálenějších regionů.</b></li>
            </ul>


            <h2 class="header">Jak to funguje pro firmy?</h2>
            <p>
                Firma může vyplnit úplně stejně svůj profil, jako škola. Navíc si však může vybrat
                střední školy a učiliště, se kterými spolupracuje. (Seznam spolupracujících škol se
                bude zobrazovat v jejím profilu.)
            </p>
            <p>
                Odkaz na firmu se bude navíc zobrazovat i v profilu jednotlivých škol.
            </p>
            <p>
                Firma si může objednat výstavu stejně jako škola. Pro každou výstavu vloží odkaz na svoji on-line
                schůzku, kde může komunikovat. Firmy mohou využít svůj komunikační kanál i pro domluvení budoucí
                spolupráce s dalšími školami, případně je mohou přes něj oslovit zájemci o zaměstnání.
            </p>

            <h2 class="header">Jak to funguje pro úřady?</h2>
            <p>
                Úřady (Úřady práce, Hospodářské komory a další) se prozatím registrují jako firmy. Nevyplňují žádné
                spolupracující školy. Přes víkend jim doděláme samostatnou kategorii.
            </p>

            <h2 class="header">Cena</h2>
            <p>
                <b>První on-line burzu bude mít každý vystavovatel zdarma. Bude si moci vyzkoušet,
                    zda to technicky funguje</b>, zda se deváťáci a jejich rodiče skutečně budou připojovat,
                zkrátka zda to má pro vystavovatele skutečně přínos.
            </p>
            <p>
                <b>Teprve za každou další burzu zaplatí vystavovatel 1.000,- Kč.</b>
            </p>
            <p>
                Finančně to vychází ve srovnání s prezenčními burzami více než přijatelně. 🙂 A my aspoň zaplatíme našim
                studentům za naprogramování portálu a když se to jó podaří, tak třeba vyměníme v naší škole okna. :-)
            </p>
            <p>
                Fakturu je třeba zaplatit před termínem objednaných výstav, jinak nebude odkaz na on-line schůzku
                aktivní. (Doporučujeme platit s předstihem aspoň 3 pracovní dny před konáním první výstavy.) Odkaz na
                on-line schůzku se „zaktivní“ vždy až v den konání výstavy, aby se žáci zbytečně nepřipojovali k
                „neobsazeným“ schůzkám.
            </p>

